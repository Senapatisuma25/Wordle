function deleteletter()
{
   const letterelements=wordelements[rows-1].querySelectorAll('.col')
   for(let index=letterelements.length-1;index>=0;index--)
   {
    const element=letterelements[index];
    if(element.innerText!=='')
    {
        element.innerText='';
        letter-=1;
        break;
    }
   }
}
function checkword(){
   const letterelements=wordelements[rows-1].querySelectorAll('.col')
   let noofcorrectalphabets=0;
   letterelements.forEach((element,index)=>{
    let letter=element.innerText.toLowerCase();
    if(word[index]==letter)
    {
        element.classList.add('word-green');
         noofcorrectalphabets+=1;
    }
    else if(word.includes(letter))
    {
        element.classList.add('word-yellow');
    }
    else{
        element.classList.add('word-grey');
    }
   });
    if(rows==6){
    gameover=true;
    text.innerText=`ANS -> ${word}`;
   }
   else if(noofcorrectalphabets==5){
   gameover=true;
   guesscorrectly=true;
   text.innerText='Congratulations,You won!!';
   }
}
